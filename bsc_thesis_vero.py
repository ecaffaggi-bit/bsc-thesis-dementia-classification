# Pytorch dataset
class OASISDataset(Dataset):

    # Loads .png slices
    def __init__(self, subject_df, dataset_root, transform=None):

        self.transform    = transform
        self.samples      = []  # list of (image_path, label) tuples

        for _, row in subject_df.iterrows():
            subj  = row["subject"]
            label = row["label"]

            # Get all slices for this subject
            slices = glob.glob(f"{dataset_root}/{label}/{subj}_slice_*.png")

            for slice_path in sorted(slices):
                self.samples.append((slice_path, LABEL_MAP[label]))

        print(f"Dataset loaded: {len(self.samples)} slices | "
              f"dementia: {sum(1 for _,l in self.samples if l==1)} | "
              f"nondementia: {sum(1 for _,l in self.samples if l==0)}")

    # How many images in my dataset
    def __len__(self):
        return len(self.samples)

    # Loading one sample
    def __getitem__(self, idx):

      """Returns:
      - subject ID
      - image
      - label
      to enable subject-level analysis and no data leakage between splits"""

      img_path, label = self.samples[idx]

      # Extract subject ID from filename
      subj = os.path.basename(img_path).split("_slice_")[0]

      image = Image.open(img_path).convert("RGB")
      if self.transform:
          image = self.transform(image)

      return image, torch.tensor(label, dtype=torch.long), subj

