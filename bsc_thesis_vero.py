# Training loop: Experiment 3 - no early stopping, 25 epochs
def train_model(model, train_loader, val_loader, criterion, optimizer,
                num_epochs=25, scheduler=None, save_path="/content/drive/MyDrive/BSC THESIS ALZHEIMERS/best4_model.pth"):


    """Trains a model for a fixed number of epochs and saves the best checkpoint.
    Compatible with OASISDataset which returns (image, label, subject)

    Args:
    model = either renset-18 or medvit
    train_loader, val_loader
    criterion = loss function
    otimizer = optimization technique (Adam)
    epochs = 1st experiment 50, 2nd experiment onwards 25
    scheduler = multistepLR

    Returns:
    Tracking history of validation and training losses and accuracies

    """

    best_val_loss = float('inf')
    history = {"train_loss": [], "val_loss": [], "train_acc": [], "val_acc": []}

    for epoch in range(num_epochs):

        # Setting the model to training mode
        model.train()
        running_loss, running_corrects = 0.0, 0

        # Perform forward-backward passes and accumulate losses and accuracies
        for inputs, labels, _ in train_loader:
            inputs, labels = inputs.to(device), labels.to(device)

            optimizer.zero_grad()
            outputs = model(inputs)

            _, preds = torch.max(outputs, 1)
            loss = criterion(outputs, labels)

            loss.backward()
            optimizer.step()

            running_loss += loss.item() * inputs.size(0)
            running_corrects += torch.sum(preds == labels.data)

        train_loss = running_loss / len(train_loader.dataset)
        train_acc = running_corrects.float() / len(train_loader.dataset)

        # Setting the model to evaluation mode
        model.eval()
        running_loss = 0.0
        running_corrects = 0


        # Compute the predictions and the statistics (losses, and accuracies)
        with torch.no_grad():
            for inputs, labels, _ in val_loader:
                inputs = inputs.to(device)
                labels = labels.to(device)

                outputs = model(inputs)
                _, preds = torch.max(outputs, 1)
                loss = criterion(outputs, labels)

                running_loss += loss.item() * inputs.size(0)
                running_corrects += torch.sum(preds == labels.data)

        val_loss = running_loss / len(val_loader.dataset)
        val_acc = running_corrects.float() / len(val_loader.dataset)

        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["train_acc"].append(train_acc.item())
        history["val_acc"].append(val_acc.item())

        print(f"Epoch [{epoch+1}/{num_epochs}]  "
              f"train loss: {train_loss:.4f}  train acc: {train_acc:.4f}  "
              f"val loss: {val_loss:.4f}  val acc: {val_acc:.4f}")

        if scheduler is not None:
            scheduler.step()

        # Save best model only
        if val_loss < best_val_loss:
            best_val_loss = val_loss
            torch.save(model.state_dict(), save_path)
            print(f"  Best model saved (val loss: {best_val_loss:.4f})")

    print("\nTraining complete.")
    return history

# Evaluation loop
def evaluate_model(model, test_loader, checkpoint_path, device):

  """Loads the best checkpoint and evaluates the model on the test set
  Returns accuracy, precision, recall, F1, and AUC """

  model.load_state_dict(torch.load(checkpoint_path, map_location=device))
  model.eval()

  all_labels = []
  all_preds  = []
  all_probs  = []   # needed for AUC

  with torch.no_grad():
      for inputs, labels, _ in test_loader:
          inputs = inputs.to(device)
          labels = labels.to(device)

          outputs = model(inputs)
          probs   = torch.softmax(outputs, dim=1)[:, 1]  # prob of dementia class
          _, preds = torch.max(outputs, 1)

          all_labels.extend(labels.cpu().numpy())
          all_preds.extend(preds.cpu().numpy())
          all_probs.extend(probs.cpu().numpy())

  acc  = accuracy_score(all_labels, all_preds)
  prec = precision_score(all_labels, all_preds, zero_division=0)
  rec  = recall_score(all_labels, all_preds, zero_division=0)
  f1   = f1_score(all_labels, all_preds, zero_division=0)
  auc  = roc_auc_score(all_labels, all_probs)

  print("=" * 45)
  print(f"  Accuracy:  {acc:.4f}")
  print(f"  Precision: {prec:.4f}")
  print(f"  Recall:    {rec:.4f}")
  print(f"  F1-Score:  {f1:.4f}")
  print(f"  AUC-ROC:   {auc:.4f}")
  print("=" * 45)

  return {"accuracy": acc, "precision": prec, "recall": rec, "f1": f1, "auc": auc}

