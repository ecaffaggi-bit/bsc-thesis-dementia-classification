def show_gradcam(model, checkpoint_path, dataset, device, title, target_layers):
    model.load_state_dict(torch.load(checkpoint_path, map_location=device, weights_only=False))
    model.eval()

    cam = GradCAM(model=model, target_layers=target_layers)

    # 2 dementia + 2 non-dementia
    dem_indices    = [i for i in range(len(dataset)) if dataset[i][1] == 1][:2]
    nondem_indices = [i for i in range(len(dataset)) if dataset[i][1] == 0][:2]
    indices = dem_indices + nondem_indices

    fig, axes = plt.subplots(4, 3, figsize=(12, 16))
    fig.suptitle(f"Grad-CAM — {title}", fontsize=14, fontweight="bold")

    for row, idx in enumerate(indices):
        img_tensor, label, subj = dataset[idx]
        input_tensor = img_tensor.unsqueeze(0).to(device)

        with torch.no_grad():
            output = model(input_tensor)
            pred = torch.argmax(output, 1).item()

        targets = [ClassifierOutputTarget(pred)]
        grayscale_cam = cam(input_tensor=input_tensor, targets=targets)[0]

        img_np = img_tensor.permute(1, 2, 0).numpy()
        img_np = (img_np - img_np.min()) / (img_np.max() - img_np.min())
        cam_image = show_cam_on_image(img_np.astype(np.float32), grayscale_cam, use_rgb=True)

        label_str = "Dementia"     if label == 1 else "Non-Dementia"
        pred_str  = "Dementia"     if pred  == 1 else "Non-Dementia"

        axes[row, 0].imshow(img_np, cmap="gray")
        axes[row, 0].set_title(f"Original\nTrue: {label_str}", fontsize=10)
        axes[row, 0].axis("off")
        axes[row, 1].imshow(grayscale_cam, cmap="jet")
        axes[row, 1].set_title("CAM heatmap", fontsize=10)
        axes[row, 1].axis("off")
        axes[row, 2].imshow(cam_image)
        axes[row, 2].set_title(f"Overlay\nPred: {pred_str}", fontsize=10)
        axes[row, 2].axis("off")

    plt.tight_layout()
    save_path = f"/content/drive/MyDrive/BSC THESIS ALZHEIMERS/gradcam_{title.replace('-','').replace(' ','_')}_v2.png"
    plt.savefig(save_path, dpi=150)
    plt.show()
    print(f"Saved: {save_path}")

