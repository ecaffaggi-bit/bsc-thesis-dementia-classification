def find_example_subjects(t88_files, labels, n=1):
    demented_paths    = []
    nondemented_paths = []
    for hdr_path in t88_files:
        filename = os.path.basename(hdr_path)
        subj = filename.split("_MR")[0]
        if subj not in labels:
            continue
        if labels[subj] == "dementia" and len(demented_paths) < n:
            demented_paths.append((subj, hdr_path))
        elif labels[subj] == "nondementia" and len(nondemented_paths) < n:
            nondemented_paths.append((subj, hdr_path))
        if len(demented_paths) == n and len(nondemented_paths) == n:
            break
    return demented_paths, nondemented_paths

