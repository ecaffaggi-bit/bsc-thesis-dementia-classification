# Functions for preprocessing
def zscore_normalize(vol):
    """Z-score normalize a 3D MRI volume to ensure zero-mean and unit-variance intensity
    across the whole volume"""

    mean = np.mean(vol)
    std = np.std(vol)
    return (vol - mean) / (std + 1e-8)


def get_center_slices(vol, center=88, n=10):

  """Only taking 10 centered slices centered at Slice 88 because
  in Talairach space it corresponds to the hippocampal region (Saratxaga et al. (2021))"""

  start = center - n // 2
  end   = center + n // 2

  # Take everything in X and Y at depth Z=i
  # Since we want the shape to be 2D so (X,Y) and we want axial slices so Z
  return [(i, vol[:, :, i]) for i in range(start, min(end, vol.shape[2]))]

